# Инициализация матриц Z и D
Z = []
D = []

while True:
    # Запрашиваем ввод элемента строки
    row_input = input("Введите строку элементов через пробел или нажмите Enter для завершения матрицы Z: ")

    # Проверяем, был ли введен элемент строки
    if row_input:
        # Разделяем введенные элементы и добавляем их в список
        row = list(map(int, row_input.split()))
        Z.append(row)
    else:
        # Если введена пустая строка, завершаем ввод
        break

while True:
    # Запрашиваем ввод элемента строки
    row_input = input("Введите строку элементов через пробел или нажмите Enter для завершения матрицы D: ")

    # Проверяем, был ли введен элемент строки
    if row_input:
        # Разделяем введенные элементы и добавляем их в список
        row = list(map(int, row_input.split()))
        D.append(row)
    else:
        # Если введена пустая строка, завершаем ввод
        break

# Подсчет количества элементов, превышающих суммы элементов соответствующих строк матрицы Z
G = [sum(D[i][j] > sum(Z[i]) for j in range(len(Z[i]))) for i in range(len(Z))]

# Умножение матрицы D на максимальный элемент массива G
D_transformed = [[D[i][j] * max(G) for j in range(len(D[i]))] for i in range(len(D))]

# Вывод матриц Z, D до и после преобразования, а также массив G
print("Матрица Z:")
for row in Z:
    print(*row)
print("\nМатрица D до преобразования:")
for row in D:
    print(*row)
print("\nМатрица D после преобразования:")
for row in D_transformed:
    print(*row)
print("\nМассив G:")
print(*G)
